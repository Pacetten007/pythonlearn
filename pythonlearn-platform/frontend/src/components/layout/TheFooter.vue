const changeLanguage = (language) => {
  currentLanguage.value = language
  closeLanguageMenu()
  
  // –í —Ä–µ–∞–ª—å–Ω–æ–º –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–∏ –∑–¥–µ—Å—å –±—ã–ª–∞ –±—ã —Å–º–µ–Ω–∞ –ª–æ–∫–∞–ª–∏–∑–∞—Ü–∏–∏
